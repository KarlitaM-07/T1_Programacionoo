package Biblioteca;

public class Usuario {
	
	String nombre;
	int edad;
	int Ncedula;
	
	String getNombre() {
		return nombre;
		
	}
	
	void setNombre(String nombre) {
		this.nombre=nombre;
		
	}
	int getEdad() {
		return edad;	
		
	}
	
	void setEdad(int edad) {
		this.edad=edad;
		
	}
	int getNcedula() {
		return Ncedula;
		
	}
	
	void SetNcedula(int Ncedula) {
		this.Ncedula=Ncedula;
		
	}
}
